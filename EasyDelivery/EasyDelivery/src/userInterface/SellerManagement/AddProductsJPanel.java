    public void populateDepartmentCombo() {
        comboBoxDepartment.removeAllItems();
        for (Department department : system.getDepartmentCatalog().getDepartmentList()) {
            comboBoxDepartment.addItem(department);
        }
    }

    public void populateSubDepartmentCombo(Department subdepartment) {
        comboBoxSubdepartment.removeAllItems();
        for (Department department : subdepartment.getDepartmentList()) {
            comboBoxSubdepartment.addItem(department);
        }
    }

