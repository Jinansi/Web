    private void btnAddImageActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddImageActionPerformed
        JFileChooser jFileChooser = new JFileChooser();
        jFileChooser.showOpenDialog(null);
        try {
            bufferedimage = ImageIO.read(jFileChooser.getSelectedFile());
        } catch (IOException ex) {

        }

        ImageIcon imageIcon = new ImageIcon(bufferedimage);
        lblImage.setIcon(new ImageIcon(imageIcon.getImage().getScaledInstance(212, 122, java.awt.Image.SCALE_FAST)));
    }//GEN-LAST:event_btnAddImageActionPerformed

    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        if (txtModelnumber.getText().equals("") && txtPrice.getText().equals("") && txtProductName.getText().equals("")
                && txtQuantity.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Enter All mandatory fields.");
        } else {
            if (RegexUtils.isAlphaNumeric(txtModelnumber.getText(), "Check model number") && RegexUtils.isDouble(txtPrice.getText(), "Check Price")
                    && RegexUtils.isText(txtProductName.getText(), "Check Product name") && RegexUtils.isInteger(txtQuantity.getText(), "Check Quantity")) {

                Product pr = new Product(txtProductName.getText());
                pr.setPrice(Float.parseFloat(txtPrice.getText()));
                pr.setModelNo(txtModelnumber.getText());
                pr.setBrand(txtBrand.getText());
                pr.setImage((ImageIcon) lblImage.getIcon());

                MasterProductCatalog mpc = new MasterProductCatalog();
                mpc.getProductList().add(pr);

                SKU sku = new SKU(pr);
                sku.setQuantity(Integer.parseInt(txtQuantity.getText()));

                org.getSkuCatalog().addSKU(sku);

                Department subdept = (Department) comboBoxSubdepartment.getSelectedItem();
                subdept.getProductList().add(pr);

                JOptionPane.showMessageDialog(null, "Your Product has been added Successfully.");
                txtBrand.setText("");
                txtModelnumber.setText("");
                txtPrice.setText("");
                txtProductName.setText("");
                txtQuantity.setText("");
            }
        }
    }//GEN-LAST:event_btnSubmitActionPerformed

