    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblDepartment.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select a row to delete!", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {
            int dialogButton = JOptionPane.YES_NO_OPTION;
            int dialogResult = JOptionPane.showConfirmDialog(null, "Do yo want to delete the details", "Warning", dialogButton);
            if (dialogResult == JOptionPane.YES_OPTION) {
                Department dpt = (Department) tblDepartment.getValueAt(selectedRow, 0);
                Department subdpt = (Department) tblDepartment.getValueAt(selectedRow, 1);
                dpt.getDepartmentList().remove(subdpt);
                populateDepartmentTable();
            }

        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnAddDeptActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddDeptActionPerformed
        // TODO add your handling code here:

        String dpt = txtDepartment.getText();
        String subDept = txtSubDpt.getText();
        if (RegexUtils.isEmpty(dpt, "Please enter value for Department! is empty")) {
            return;
        }
        if (RegexUtils.isEmpty(subDept, "Please enter value for Sub Department is empty")) {
            return;
        }
        if (!RegexUtils.isText(dpt, "Please enter valid Department")) {
            return;
        }
        if (!RegexUtils.isText(subDept, "Please enter valid Sub Department")) {
            return;
        }
        
        Department dept;
        Department sub;
        if(system.getDepartmentCatalog().checkIfdepartmentNameIsUnique(dpt)){
            dept = new Department(dpt);
            dept.setName(dpt);
        
            if(dept.checkIfsubdepartmentNameIsUnique(dpt)){
                sub= new Department(subDept);
                sub.setName(subDept);
                dept.getDepartmentList().add(sub);
                system.getDepartmentCatalog().getDepartmentList().add(dept);
            }
        
        }else{
            JOptionPane.showMessageDialog(null, "This department already exists");
            return;
        }
        
        populateDepartmentTable();
        txtDepartment.setText("");
        txtSubDpt.setText("");
        JOptionPane.showMessageDialog(null, "Department added successfully!!", "Info", JOptionPane.INFORMATION_MESSAGE);

    }//GEN-LAST:event_btnAddDeptActionPerformed

