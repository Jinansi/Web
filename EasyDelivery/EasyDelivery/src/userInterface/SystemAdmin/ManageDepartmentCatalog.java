    private void populateDepartmentTable() {
        DefaultTableModel model = (DefaultTableModel) tblDepartment.getModel();
        model.setRowCount(0);
        tblDepartment.setRowHeight(80);
        for (Department department : system.getDepartmentCatalog().getDepartmentList()) {
            for (Department subdepartment : department.getDepartmentList()) {
                Object[] row = new Object[3];
                row[0] = department;
                row[1] = subdepartment;
                model.addRow(row);
            }
        }
    }

